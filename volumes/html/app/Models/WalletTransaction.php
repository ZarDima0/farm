<?php

namespace App\Models;

use Illuminate\Database\Eloquent\Factories\HasFactory;
use Illuminate\Database\Eloquent\Model;

/**
 * @property int $id
 * @property int $user_id
 * @property int $gem_amount
 * @property string $type
 * @property string $status
 */
class WalletTransaction extends Model
{
    public const TYPE_ADD_GEM = 'replenishment';
    public const TYPE_SUCCEEDED = 'succeeded';
    public const TYPE_WRITE_OFF = 'cansel';
    public const TYPE_BUY = 'buy';

    protected $guarded = ['id'];

    /**
     * @return int
     */
    public function getId(): int
    {
        return $this->id;
    }

    /**
     * @param int $id
     */
    public function setId(int $id): void
    {
        $this->id = $id;
    }

    /**
     * @return int
     */
    public function getUserId(): int
    {
        return $this->user_id;
    }

    /**
     * @param int $user_id
     */
    public function setUserId(int $user_id): void
    {
        $this->user_id = $user_id;
    }

    /**
     * @return int
     */
    public function getGemAmount(): int
    {
        return $this->gem_amount;
    }

    /**
     * @param int $gem_amount
     */
    public function setGemAmount(int $gem_amount): void
    {
        $this->gem_amount = $gem_amount;
    }

    /**
     * @return string
     */
    public function getType(): string
    {
        return $this->type;
    }

    /**
     * @param string $type
     */
    public function setType(string $type): void
    {
        $this->type = $type;
    }

    /**
     * @return string
     */
    public function getStatus(): string
    {
        return $this->status;
    }

    /**
     * @param string $status
     */
    public function setStatus(string $status): void
    {
        $this->status = $status;
    }
}
